a = 10
b = 20

less_than = a < b
print(a, "is less than", b, ":", less_than)

greater_than = a > b
print(a, "is greater than", b, ":", greater_than)

less_than_equal = a <= b
print(a, "is less than or equal to", b, ":", less_than_equal)

greater_than_equal = a >= b
print(a, "is greater than or equal to", b, ":", greater_than_equal)

equal_to = a == b
print(a, "is equal to", b, ":", equal_to)

not_equal_to = a != b
print(a, "is not equal to", b, ":", not_equal_to)